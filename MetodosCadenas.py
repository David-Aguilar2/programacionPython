nombre="david alexander aguilar barrientos"

#si es minúscula la primera letra en cada palabra, la vuelve mayúscula 
print(nombre.title())

#Texto en mayúscula 
print(nombre.upper())

#esta lo pasa a minúsculas nuevamente 
print(nombre.upper().lower())

#convierte solo la primera letra dela cadena en mayúscula
print(nombre.capitalize())

#Contar cuantas veces se repite la misma palabra 
print(nombre.count("a"))

